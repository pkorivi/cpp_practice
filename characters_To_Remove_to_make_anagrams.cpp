int number_needed(string a, string b) {
    //cout<<a<<"  "<<b<<endl;
    sort(a.begin(),a.end());
    sort(b.begin(),b.end());
    string intersection;
    set_intersection(a.begin(),a.end(),b.begin(),b.end(),back_inserter(intersection));
    return a.size()+b.size()-2*(intersection.size());
}

